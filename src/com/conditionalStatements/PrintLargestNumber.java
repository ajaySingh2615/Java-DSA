package com.conditionalStatements;

public class PrintLargestNumber {
    public static void main(String[] args) {
        int a = 20;
        int b = 10;

        if(a > b){
            System.out.println(a + " greater than " + b);
        }else{
            System.out.println(b + " greater than " + a);
        }
    }
}
